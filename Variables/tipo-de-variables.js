/* 
    LET
    las variables definida con let solo viven dentro de un 
    bloque de codigo.
    Puede cambiar su valor
*/

let numero = 15;
numero = 18;
numero = 385;


/* 
    CONST
    Las variables se inicializan 1 sola vez con
     "const" .Las constantes no pueden cambiar su valor
*/
const numeroPi = 3.14;
