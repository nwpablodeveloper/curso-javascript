/* 
    Los operadores aritméticos toman valores numéricos
    ( ya sean literales o variables ) como sus operandos
    y retornan un valor nemérico único

*/

// --------------------------------------------------------

let num1 = 50;
let num2 = 10;

let resultado = num1 + num2;  // Resultado 65
let resultado2 = num1 - num2;  // Resultado 40

// --------------------------------------------------------

let num3 = 35;
num3++ 
// Resultado num3 = 36


// --------------------------------------------------------

let num4 = 40;
alert(num4--); // Resultado 40


let num5 = 40;
num5--;
alert(num5) // Resultado 39
// debemos hacer la operación aritmetica antes de mostrar el valor

let num6 = 40;
resultado = num6++ // num6 va a seguir valiendo 40

let num7 = 40;
num7++;
resultado = num7; // num7 = 41

// --------------------------------------------------------

